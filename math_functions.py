x = 3.14
y = 4
z = 5

# round function
result = round(x)
print(result)

# absolute value function - is a distance away from zero as a whole number
result = abs(y)
print(result)

# power function (число в ступені)
result = pow(4, 3)
print(result)

# max, min value
result = min(x, y, z)
print(result)
result = max(x, y, z)
print(result)
